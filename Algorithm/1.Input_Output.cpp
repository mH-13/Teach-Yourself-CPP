#include <iostream>
using namespace std;

int main() {
    // Input and Output
    int a, b;
    cout << "Enter two numbers: ";
    cin >> a >> b;

    // Basic Operations
    cout << "Sum: " << a + b << endl;
    cout << "Difference: " << a - b << endl;
    cout << "Product: " << a * b << endl;

    return 0;
}
