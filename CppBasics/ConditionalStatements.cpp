#include <iostream>

int main() {
    int x = 10;
    int y = 20;

    // Conditional statements to compare x and y
    if (x > y) {
        std::cout << "x is greater than y" << std::endl;
    } else if (x < y) {
        std::cout << "x is less than y" << std::endl;
    } else {
        std::cout << "x is equal to y" << std::endl;
    }

    return 0;
}