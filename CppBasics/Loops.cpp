//for
#include <iostream>

int main() {
    // For loop to print values from 0 to 4
    for (int i = 0; i < 5; i++) {
        std::cout << "i = " << i << std::endl;
    }
    return 0;
}

//while
#include <iostream>

int main() {
    int i = 0;

    // While loop to print values from 0 to 4
    while (i < 5) {
        std::cout << "i = " << i << std::endl;
        i++;
    }
    return 0;
}